function togglePlay() {
  if (isPlaying) {
    pauseSong();
  } else {
    playSong();
  }
}


function nextSong() {
  currentSongIndex = (currentSongIndex + 1) % playlist.length;
  loadSong(currentSongIndex);
  playSong();
}

function prevSong() {
  currentSongIndex = (currentSongIndex - 1 + playlist.length) % playlist.length;
  loadSong(currentSongIndex);
  playSong();
}

function selectSong(index) {
  currentSongIndex = index;
  loadSong(currentSongIndex);
  playSong();
}

